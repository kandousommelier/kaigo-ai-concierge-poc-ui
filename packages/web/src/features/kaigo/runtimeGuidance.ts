export const getKaigoRuntimeGuidance = (input: string): string => {
  const normalizedInput = input.toLowerCase();

  if (isRecordForeignStaffConsultation(normalizedInput)) {
    return RECORD_FOREIGN_STAFF_GUIDANCE;
  }

  if (isWatchSubsidyConsultation(normalizedInput)) {
    return WATCH_SUBSIDY_GUIDANCE;
  }

  if (isIctAdoptionConsultation(normalizedInput)) {
    return ICT_ADOPTION_GUIDANCE;
  }

  if (isProductivityCommitteeConsultation(normalizedInput)) {
    return PRODUCTIVITY_COMMITTEE_GUIDANCE;
  }

  return '';
};
