const includesAny = (input: string, keywords: string[]) =>
  keywords.some((keyword) => input.includes(keyword));

const isRecordForeignStaffConsultation = (input: string) => {
  const hasRecordContext = includesAny(input, [
    '介護記録',
    '記録',
    '入力内容',
    'フィードバック',
    'リーダーが修正',
  ]);
  const hasForeignStaffContext = includesAny(input, [
    '外国人職員',
    '外国人',
    '技能実習',
    '特定技能',
    '日本語',
  ]);
  const hasCorrectionContext = includesAny(input, ['修正', 'リーダーが修正']);

  return hasRecordContext && (hasForeignStaffContext || hasCorrectionContext);
};

const isWatchSubsidyConsultation = (input: string) => {
  const hasWatchOrDeviceContext = includesAny(input, [
    '見守り',
    '見守り機器',
    '見守りセンサー',
    '眠りscan',
    '眠りスキャン',
    'ミリ波',
    'センサー',
    '離床',
    '夜間巡視',
    '展示会',
    '介護ロボット',
  ]);
  const hasSubsidyContext = input.includes('補助金');
  const hasSubsidyRelatedContext = includesAny(input, [
    '介護ロボット',
    '見守り',
    'ict',
    '導入',
    '機器',
    '展示会',
    'センサー',
  ]);

  return hasWatchOrDeviceContext || (hasSubsidyContext && hasSubsidyRelatedContext);
};

